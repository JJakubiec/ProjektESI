
class Node:
    def __init__(self):
        self.name = None
        self.left_child = None
        self.right_child = None

    def set_name(self, name):
        self.name = name

    def set_left_child(self, left_child):
        self.left_child = left_child

    def set_right_child(self, right_child):
        self.right_child = right_child

